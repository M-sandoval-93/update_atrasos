export let LibreriaFunciones = {
    // Valida el rut con su cadena completa "XXXXXXXX-X"
    //  ^(\d{1}|\d{2})\.(\d{3}\.\d{3}-)([a-zA-Z]{1}$|\d{1}$) // expreion regular para rut
    // expresion para validar lonjitud y formato
    // ^(\d{7}|\d{8}|\d{9})\-([a-zA-Z]{1}$|\d{1}$)
    validarRut: (rutCompleto) => {
        if (!/^[0-9]+[-|‐]{1}[0-9kK]{1}$/.test( rutCompleto )) {
            return false;
        }
        
        let tmp = rutCompleto.split('-');
        let rut = tmp[0];
        let dvRut = tmp[1];

        if (dvRut == 'K') {
            dvRut = 'K';
        }
        return (LibreriaFunciones.dv(rut) == dvRut);
    },

    // Calcula el dígito verificador
    dv: (T) => {
        let M = 0, S = 1;

        for (;T;T = Math.floor(T/10)) {
            S = (S + T % 10 * (9 - M ++ % 6)) % 11;
        }
        return S?S - 1: 'K';
    },

    // Valida que el número sea un entero
    validarEntero: (value) => {
        let regExPattern = /[0-9]+$/;
        return regExPattern.test(value);
    },

    // Formatea un número con puntos de miles
    formatearNumero: (val) => {
        if (LibreriaFunciones.validarEntero(val)) {
            let retorno = '';
            let value = val.toString().split('').reverse().join('');
            let i = value.length;

            while (i > 0) {
                retorno += ((i%3==0&&i!=value.length)?'':'')+value.substring(i--,i);
                // retorno += ((i%3==0&&i!=value.length)?'.':'')+value.substring(i--,i);  //Para ir agregando el punto
            }
            return retorno;
        }
        return val;
    },

    // Comprobar longitud del rut
    comprobarLongitudRut: function() {
        let contador = 0;
        $('.input-rut').each(function() {
            if ($(this).val().length > 1 && $(this).val().length < 8) {
                contador = contador + 1;
            }
        });

        return contador;
    },

    // Alertas de sweetAlert2 predefinidas
    alertPopUp: (icon, title) => {
        Swal.fire({
            icon: icon,
            title: title,
            showConfirmButton: false,
            timer: 1500
        });
    },

    restarFechas: (f1, f2) => { // revisar como hacer que se resten solo los dias hábiles, sin contar fines de semana y feriados, ver uso de api
        if (f1.val() != '' && f2.val() != '') {
            let afecha1 = f1.val().split('-');
            let afecha2 = f2.val().split('-');
            let fFecha1 = Date.UTC(afecha1[0],afecha1[1]-1,afecha1[2]); 
            let fFecha2 = Date.UTC(afecha2[0],afecha2[1]-1,afecha2[2]); 
            let dif = fFecha2 - fFecha1;
            let dias = Math.floor(dif / (1000 * 60 * 60 * 24)) + 1; 
            return dias;
        }    
    },

    alertBoostrap: (elemento, message, type) => {
        let componente = document.getElementById(elemento);

        let wrapper = document.createElement('div');
        wrapper.innerHTML = '<div class="alert alert-' + type + ' alert-dismissible" role="alert">' + message + '</div>';
        componente.append(wrapper);
    },

    autoFocus: (modal, input) => {
        modal.on('shown.bs.modal', function() {
            input.focus();
        });
    },


    loadApoderado: (rut, campo) => {
        let datos = 'getApoderado';

        $.ajax({
            url: "./controller/controller_apoderado.php",
            type: "post",
            dataType: "json",
            data: {datos: datos, rut: rut},
            success: (data) => {
                $(campo).html(data);
            }
        });
    },

    validarNumberRut: (rut, informacion = true) => {
        rut.val(rut.val().replace(/[^0-9]/g, ''));

        if (rut.val().length > 9 && rut.val() != '') {
            rut.addClass('is-invalid');
            $('#rut_estudiante_atraso').addClass('is-invalid');
            if (informacion != true) {
                informacion.removeClass('form-text');
                informacion.text('Rut inválido, revisar !!');
                informacion.addClass('text-danger');
            }
        } else {
            rut.removeClass('is-invalid');
            if (informacion != true) {
                informacion.removeClass('text-danger');
                informacion.text('Rut sin puntos, sin guión y sin dígito verificador');
                informacion.addClass('form-text')
            }
        }
    }

}


export function generar_dv(rut, dv_rut) {
    // Traspasar valor a número entero
    let numero = $(rut).val();
    numero = numero.split('.').join('');

    // Valida que sea realmente entero
    if (LibreriaFunciones.validarEntero(numero)) {
        $(dv_rut).val(LibreriaFunciones.dv(numero));

    } else {
        $(dv_rut).val('');
    }

    // Formatear el valor del rut con sus puntos
    $(rut).val(LibreriaFunciones.formatearNumero(numero));
}
